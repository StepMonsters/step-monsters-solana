pub fn handle_monster_feature_config(config: [[u16; 7]; 64]) -> Vec<Vec<[u16; 7]>> {
    let mut sum = 0;
    let mut features = Vec::new();
    let mut temp = Vec::new();
    for i in 0..config.len() {
        if config[i][0] == 0 {
            continue;
        };
        sum += config[i][0];
        if sum < 1000 {
            temp.push(config[i].clone());
        } else if sum == 1000 {
            features.push(temp.clone());
            temp = Vec::new();
            sum = 0;
        } else {
            panic!("Invalid config.");
        }
    }
    return features;
}